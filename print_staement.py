name="mehedi" 
age=21

#using commas
print(name)
print("Age",age,"Name:",name)
#using a new line
print("Age",age,"\nName:",name)
#using +
print("Name: "+name +" Age: "+str(age))
#using f-string
print(f"Name:{name} Age:{age}")
print(f"My name is {name}")

#using format
print("My name is {}".format(name))
print("My name is {} & My age is {}".format(name,age))

#addition

x=10
y=20

print(f"Sum is {x+y}")